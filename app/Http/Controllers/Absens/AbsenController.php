    {

        $userName = auth()->user()->name;
        $barangs = Absen::whereDate('created_at', today())->orderBy('created_at', 'DESC')->take(5)
            ->get();
        $siswa = Absen::whereDate('created_at', today())->where('name', $userName)->latest()->get();
        $mata_pelajarans = MataPelajaran::orderBy('name', 'ASC')->get();

        return view('absens.index', compact('barangs', 'siswa', 'mata_pelajarans'));
    }


